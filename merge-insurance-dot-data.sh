    c.TRMLIMO_16, c.TRPTRUCK, c.TRPTRACT, c.TRPTRAIL, c.TRPCOACH,
    c.TRPSCHOOL_1_8, c.TRPSCHOOL_9_15, c.TRPSCHOOL_16, c.TRPBUS_16, c.TRPVAN_1_8,
    c.TRPVAN_9_15, c.TRPLIMO_1_8, c.TRPLIMO_9_15, c.TRPLIMO_16, c.DOCKET1_STATUS_CODE,
    c.DOCKET2_STATUS_CODE, c.DOCKET3_STATUS_CODE,

