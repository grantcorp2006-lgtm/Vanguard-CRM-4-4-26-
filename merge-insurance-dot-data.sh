    REVIEW_DATE, SAFETY_RATING, SAFETY_RATING_DATE, UNDELIV_PHY, CRGO_GENFREIGHT,
    CRGO_HOUSEHOLD, CRGO_METALSHEET, CRGO_MOTOVEH, CRGO_DRIVETOW, CRGO_LOGPOLE,
    CRGO_BLDGMAT, CRGO_MOBILEHOME, CRGO_MACHLRG, CRGO_PRODUCE, CRGO_LIQGAS,
    CRGO_INTERMODAL, CRGO_PASSENGERS, CRGO_OILFIELD, CRGO_LIVESTOCK, CRGO_GRAINFEED,
    CRGO_COALCOKE, CRGO_MEAT, CRGO_GARBAGE, CRGO_USMAIL, CRGO_CHEM,
    CRGO_DRYBULK, CRGO_COLDFOOD, CRGO_BEVERAGES, CRGO_PAPERPROD, CRGO_UTILITY,
    CRGO_FARMSUPP, CRGO_CONSTRUCT, CRGO_WATERWELL, CRGO_CARGOOTHR, CRGO_CARGOOTHR_DESC,
    OWNTRUCK, OWNTRACT, OWNTRAIL, OWNCOACH, OWNSCHOOL_1_8,
    OWNSCHOOL_9_15, OWNSCHOOL_16, OWNBUS_16, OWNVAN_1_8, OWNVAN_9_15,
    OWNLIMO_1_8, OWNLIMO_9_15, OWNLIMO_16, TRMTRUCK, TRMTRACT,
    TRMTRAIL, TRMCOACH, TRMSCHOOL_1_8, TRMSCHOOL_9_15, TRMSCHOOL_16,
    TRMBUS_16, TRMVAN_1_8, TRMVAN_9_15, TRMLIMO_1_8, TRMLIMO_9_15,
    TRMLIMO_16, TRPTRUCK, TRPTRACT, TRPTRAIL, TRPCOACH,
