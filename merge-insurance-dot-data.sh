    c.CRGO_DRYBULK, c.CRGO_COLDFOOD, c.CRGO_BEVERAGES, c.CRGO_PAPERPROD, c.CRGO_UTILITY,
    c.CRGO_FARMSUPP, c.CRGO_CONSTRUCT, c.CRGO_WATERWELL, c.CRGO_CARGOOTHR, c.CRGO_CARGOOTHR_DESC,
    c.OWNTRUCK, c.OWNTRACT, c.OWNTRAIL, c.OWNCOACH, c.OWNSCHOOL_1_8,
    c.OWNSCHOOL_9_15, c.OWNSCHOOL_16, c.OWNBUS_16, c.OWNVAN_1_8, c.OWNVAN_9_15,
    c.OWNLIMO_1_8, c.OWNLIMO_9_15, c.OWNLIMO_16, c.TRMTRUCK, c.TRMTRACT,
    c.TRMTRAIL, c.TRMCOACH, c.TRMSCHOOL_1_8, c.TRMSCHOOL_9_15, c.TRMSCHOOL_16,
