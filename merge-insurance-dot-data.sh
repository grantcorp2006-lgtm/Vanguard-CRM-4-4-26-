PRAGMA synchronous = NORMAL;
PRAGMA cache_size = 100000;
PRAGMA temp_store = MEMORY;

-- Perform the merge using INNER JOIN (only matching records)
INSERT INTO carriers_merged (
    -- DOT columns
    MCS150_DATE, ADD_DATE, STATUS_CODE, DOT_NUMBER, DUN_BRADSTREET_NO,
    PHY_OMC_REGION, SAFETY_INV_TERR, CARRIER_OPERATION, BUSINESS_ORG_ID, MCS150_MILEAGE,
