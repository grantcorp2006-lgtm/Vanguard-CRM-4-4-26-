);

-- Import insurance data
.mode csv
.separator ","
.import $INSURANCE_FILE insurance_temp

-- Check import
SELECT 'Insurance records imported: ' || COUNT(*) FROM insurance_temp;
EOF

echo "✅ Insurance data imported to temp table"

echo ""
echo "🔄 Step 2: Merging data (this will take several minutes)..."

# Perform the merge - only records that exist in both databases
sqlite3 "$TARGET_DB" <<EOF
