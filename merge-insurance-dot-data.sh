FROM insurance_temp i
INNER JOIN (SELECT * FROM "$DOT_DB".carriers) c ON CAST(i.DOT_NUMBER AS INTEGER) = c.DOT_NUMBER;

-- Get final count
SELECT 'Records successfully merged: ' || COUNT(*) FROM carriers_merged;

-- Clean up
DROP TABLE insurance_temp;
EOF

echo ""
echo "✅ Merge completed!"

echo ""
echo "📊 DB-V3 Summary:"
sqlite3 "$TARGET_DB" "
SELECT 'Total merged records: ' || COUNT(*) FROM carriers_merged;
SELECT 'Records with insurance: ' || COUNT(*) FROM carriers_merged WHERE MC_NUMBER IS NOT NULL;
SELECT 'Records with phone: ' || COUNT(*) FROM carriers_merged WHERE PHONE IS NOT NULL;
SELECT 'Records with email: ' || COUNT(*) FROM carriers_merged WHERE EMAIL_ADDRESS IS NOT NULL;"

echo ""
echo "🏆 Top 5 states in merged database:"
sqlite3 "$TARGET_DB" "SELECT PHY_STATE, COUNT(*) as count FROM carriers_merged WHERE PHY_STATE IS NOT NULL GROUP BY PHY_STATE ORDER BY count DESC LIMIT 5;"

echo ""
echo "🎯 DB-V3 MERGE COMPLETE!"
