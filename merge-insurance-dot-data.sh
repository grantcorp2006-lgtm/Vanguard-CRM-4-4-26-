    c.MCS150_DATE, c.ADD_DATE, c.STATUS_CODE, c.DOT_NUMBER, c.DUN_BRADSTREET_NO,
    c.PHY_OMC_REGION, c.SAFETY_INV_TERR, c.CARRIER_OPERATION, c.BUSINESS_ORG_ID, c.MCS150_MILEAGE,
    c.MCS150_MILEAGE_YEAR, c.MCS151_MILEAGE, c.TOTAL_CARS, c.MCS150_UPDATE_CODE_ID, c.PRIOR_REVOKE_FLAG,
    c.PRIOR_REVOKE_DOT_NUMBER, c.PHONE, c.FAX, c.CELL_PHONE, c.COMPANY_OFFICER_1,
    c.COMPANY_OFFICER_2, c.BUSINESS_ORG_DESC, c.TRUCK_UNITS, c.POWER_UNITS, c.BUS_UNITS,
    c.FLEETSIZE, c.REVIEW_ID, c.RECORDABLE_CRASH_RATE, c.MAIL_NATIONALITY_INDICATOR, c.PHY_NATIONALITY_INDICATOR,
    c.PHY_BARRIO, c.MAIL_BARRIO, c.CARSHIP, c.DOCKET1PREFIX, c.DOCKET1,
    c.DOCKET2PREFIX, c.DOCKET2, c.DOCKET3PREFIX, c.DOCKET3, c.POINTNUM,
    c.TOTAL_INTRASTATE_DRIVERS, c.MCSIPSTEP, c.MCSIPDATE, c.HM_Ind, c.INTERSTATE_BEYOND_100_MILES,
    c.INTERSTATE_WITHIN_100_MILES, c.INTRASTATE_BEYOND_100_MILES, c.INTRASTATE_WITHIN_100_MILES, c.TOTAL_CDL, c.TOTAL_DRIVERS,
    c.AVG_DRIVERS_LEASED_PER_MONTH, c.CLASSDEF, c.LEGAL_NAME, c.DBA_NAME, c.PHY_STREET,
    c.PHY_CITY, c.PHY_COUNTRY, c.PHY_STATE, c.PHY_ZIP, c.PHY_CNTY,
    c.CARRIER_MAILING_STREET, c.CARRIER_MAILING_STATE, c.CARRIER_MAILING_CITY, c.CARRIER_MAILING_COUNTRY, c.CARRIER_MAILING_ZIP,
    c.CARRIER_MAILING_CNTY, c.CARRIER_MAILING_UND_DATE, c.DRIVER_INTER_TOTAL, c.EMAIL_ADDRESS, c.REVIEW_TYPE,
    c.REVIEW_DATE, c.SAFETY_RATING, c.SAFETY_RATING_DATE, c.UNDELIV_PHY, c.CRGO_GENFREIGHT,
    c.CRGO_HOUSEHOLD, c.CRGO_METALSHEET, c.CRGO_MOTOVEH, c.CRGO_DRIVETOW, c.CRGO_LOGPOLE,
    c.CRGO_BLDGMAT, c.CRGO_MOBILEHOME, c.CRGO_MACHLRG, c.CRGO_PRODUCE, c.CRGO_LIQGAS,
